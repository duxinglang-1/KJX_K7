VMINT vm_nvram_adjust_pkey_list(vm_nvram_root_pkey_list_t * pkey_list, vm_nvram_root_pkey_list_t * pkey_list_bak)
{
	VMUINT8 flag[ROOT_MTK_PK_LIST_SIZE];
	VMUINT8 flag_f[ROOT_MTK_PK_LIST_SIZE] = {0xFF,0xFF,0xFF,0xFF,0xFF,0xFF,0xFF,0xFF,0xFF,0xFF,0xFF,0xFF,0xFF,0xFF,0xFF,0xFF};
	VMINT isfound = TRUE;
	VMUINT16 pk_list_len = sizeof(vm_nvram_root_pkey_list_t);

	
	vm_nvram_get_key_flag(flag);
	
	if(memcmp(flag,flag_f,ROOT_MTK_PK_LIST_SIZE) != 0)
	{
		vm_nvram_root_pkey_list_t *temp;
		VMINT i,j;
		
		temp = (vm_nvram_root_pkey_list_t *)_vm_kernel_malloc( sizeof(vm_nvram_root_pkey_list_t) );
		
		if( temp == NULL )
		{
			return FALSE;
		}
		
		isfound = vm_nvram_read_record(
					NVRAM_EF_MRE_PKEY_LIST_MTK_LID,
					1,
					NVRAM_EF_MRE_PKEY_LIST_MTK_SIZE,
					(void *)temp,
					pk_list_len);

		if(isfound == FALSE)
		{
			_vm_kernel_free(temp);
			return FALSE;
		}
		
		for(i=0; i<ROOT_PK_LIST_SIZE*2; i++)
		{
			if( flag[i] != 0xFF)
			{
				j = flag[i] - 100;
				
				if(j<7)
					memcpy(&pkey_list->pkey_item[j], &temp->pkey_item, sizeof(vm_pkey_t));
				else
					memcpy(&pkey_list_bak->pkey_item[j - ROOT_PK_LIST_SIZE], &temp->pkey_item, sizeof(vm_pkey_t));
			}
		}	
		_vm_kernel_free(temp);
	}
	return isfound;
}

