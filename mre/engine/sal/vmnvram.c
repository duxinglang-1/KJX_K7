VMUINT8 vm_nvram_get_firstflag(void)
{
	VMSHORT pError;
	vm_nvram_setting_list_t setting;
	VMINT result = -1;
	setting.firstflag = MRE_NOT_FIRSTRUNNING;

	if (vm_nvram_first_running == MRE_NOSET_FIRSTRUNNING)
	{
		result = ReadRecord(NVRAM_EF_MRE_FIRST_RUNNING_LID, 1, (void*)&setting, sizeof(vm_nvram_setting_list_t), &pError);

		if (result < 0)
		{
			setting.firstflag = MRE_NOT_FIRSTRUNNING;
		}
		vm_nvram_first_running = setting.firstflag;
	}
	else
	{
		setting.firstflag = vm_nvram_first_running;
	}
	return setting.firstflag;
}

