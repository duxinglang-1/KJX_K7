VMINT vm_nvram_encrypt_get_key(VMUINT16 rec_name, VMUINT8 firstflag, VMUINT8 * key_buffer, VMINT buffer_len)
{
	VMUINT8 * buf_ptr = key_buffer;
	VMINT b = 0;

	VMCHAR strb[22];
		
	if (buf_ptr == NULL)
		return 0;

	if (firstflag == MRE_FIRSTRUNNING)
	{
		if(rec_name != NVRAM_EF_MRE_PKEY_LIST_MTK_LID)
		{
			/* this is used by SP, please modify this */
			memcpy((VMUINT8 *)buf_ptr, "vre888sh", DES_KEY_LEN);		
		}
		else
		{	
			/* this is used by MTK, not modify this */
			memcpy((VMUINT8 *)buf_ptr, "mtkmresh", DES_KEY_LEN);		
		}
		
		return DES_KEY_LEN;
	}
	else
	{
		memset((VMCHAR *)strb, 0x00, 22);
		vm_get_useragent(strb, 20);	
		b = vm_per_hash(strb);
		memcpy((VMUINT8 *)buf_ptr, (VMUINT8 *)&b, 4);
		memcpy((VMUINT8 *)(buf_ptr+4), (VMUINT8 *)&b, 4);
		return DES_KEY_LEN;
	}
}


static VMUINT8 * vm_nvram_encrypt_data(VMUINT16 rec_name, VMUINT8 * input, VMINT input_len, VMINT * output_len)
{
	VMDESHANDLE ehandle = -1;
	VMUINT8 * buffer_ptr = NULL;
	VMUINT8 * key_ptr = NULL ;

	key_ptr = (VMUINT8 *)_vm_kernel_malloc(DES_KEY_LEN * sizeof(VMUINT8));
	if (key_ptr == NULL)
		return NULL;
	
	if (vm_nvram_encrypt_get_key(rec_name, MRE_NOT_FIRSTRUNNING, key_ptr, DES_KEY_LEN) == DES_KEY_LEN)
	{		
		ehandle = vm_des_set_key(key_ptr);

		if (ehandle >= 0)
		{
			buffer_ptr = vm_des_encrypt( ehandle, input, input_len, output_len );		
			vm_des_reset_key(ehandle);
		}
		else
		{
			MMI_TRACE(MMI_MRE_TRC_MOD_VMNVRAM, TRC_MRE_VMNVRAM_347 );
		}		
	}
	_vm_kernel_free(key_ptr);
	return buffer_ptr;	
}

static VMUINT8 * vm_nvram_decrypt_data(VMUINT16 rec_name, VMUINT8 * input, VMINT input_len, VMINT * output_len)
{
	VMDESHANDLE ehandle = -1;
	VMUINT8 * buffer_ptr = NULL;
	VMUINT8 * key_ptr = NULL ;

	key_ptr = (VMUINT8 *)_vm_kernel_malloc(DES_KEY_LEN * sizeof(VMUINT8));
	if (key_ptr == NULL)
		return NULL;

	if (vm_nvram_encrypt_get_key(rec_name, vm_nvram_get_firstflag(), key_ptr, DES_KEY_LEN) == DES_KEY_LEN)
	{
		ehandle = vm_des_set_key(key_ptr);
		if (ehandle >= 0)
		{
			buffer_ptr = vm_des_decrypt( ehandle, input, input_len, output_len );	
			vm_des_reset_key(ehandle);
		}
		else
		{
			MMI_TRACE(MMI_MRE_TRC_MOD_VMNVRAM, TRC_MRE_VMNVRAM_377 );
		}
	
	}
	_vm_kernel_free(key_ptr);
	return buffer_ptr;	
}

VMINT vm_nvram_read_record(VMUINT16 rec_name, VMUINT16 rec_index, VMINT rec_size, void* read_buff, VMUINT16 buff_len)
{
	VMSHORT pError;
	VMINT result = FALSE;
	VMUINT8 * encrypt_ptr = NULL;
	

	VMUINT8 * decrypt_ptr = NULL;
	VMINT decrypt_len = 0;
	
	if ((encrypt_ptr = (VMUINT8 * )_vm_kernel_malloc(rec_size * sizeof(VMUINT8))) != NULL)
	{
		memset(encrypt_ptr, 0x00, (rec_size * sizeof(VMUINT8)));
		result = ReadRecord(rec_name, rec_index, (void*)encrypt_ptr, rec_size, &pError);
		if (result >= 0)
		{
			decrypt_ptr = vm_nvram_decrypt_data(rec_name, encrypt_ptr, rec_size, &decrypt_len);

			if (decrypt_ptr != NULL)
			{
				memset(read_buff, 0x00, buff_len);
				if (decrypt_len == buff_len)
				{
					memcpy(read_buff, decrypt_ptr, buff_len);					
					result = TRUE;
				}
				else
				{
					MMI_TRACE(MMI_MRE_TRC_MOD_VMNVRAM, TRC_MRE_VMNVRAM_413 );
					result = FALSE;
				}

				vm_free(decrypt_ptr);
			}
			else
			{
				MMI_TRACE(MMI_MRE_TRC_MOD_VMNVRAM, TRC_MRE_VMNVRAM_422 );
				result = FALSE;
			}
		}
		else
		{
			MMI_TRACE(MMI_MRE_TRC_MOD_VMNVRAM, TRC_MRE_VMNVRAM_428 , rec_name, rec_index, pError);
			result = FALSE;
		}
		_vm_kernel_free(encrypt_ptr);
	}
	else
	{
		MMI_TRACE(MMI_MRE_TRC_MOD_VMNVRAM, TRC_MRE_VMNVRAM_435 );
	}
	return result;	
}

VMINT vm_nvram_write_record(VMUINT16 rec_name, VMUINT16 rec_index, VMINT rec_size, void* write_buff, VMUINT16 buff_len)
{
	VMSHORT pError;
	VMINT result = FALSE;

	VMUINT8 * encrypt_ptr = NULL;
	VMINT encrypt_len = 0;

	encrypt_ptr = vm_nvram_encrypt_data(rec_name, write_buff, buff_len, &encrypt_len);

	if (encrypt_ptr != NULL)
	{
		if (encrypt_len == rec_size)
		{
			result = WriteRecord(rec_name, rec_index, (void*)encrypt_ptr, encrypt_len, &pError);
		}
		else
		{
			result =  VM_NVRAM_RW_ERROR;
		}
		vm_free(encrypt_ptr);
		
		if (result >= 0)
		{
			result = TRUE;
		}
		else
		{
			MMI_TRACE(MMI_MRE_TRC_MOD_VMNVRAM, TRC_MRE_VMNVRAM_470 , rec_name, rec_index, pError);
			result = FALSE;
		}		
	}
	else
	{
		MMI_TRACE(MMI_MRE_TRC_MOD_VMNVRAM, TRC_MRE_VMNVRAM_476 );
	}
	
	return result;	
}


