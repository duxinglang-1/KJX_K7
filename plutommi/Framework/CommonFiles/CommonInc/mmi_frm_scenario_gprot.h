/*****************************************************************************
 * FUNCTION
 *  mmi_frm_group_insert
 * DESCRIPTION
 *  This function is used for insert the group in scenario tree. 
 *  When the applications call this function, they need to define the base id
 *  to know where the new group should be inserted (before or after).
 *  The new group id created by mmi_frm_group_create() is assigned in 
 *  mmi_frm_node_struct. If the applications want to assign more information
 *  (ex. user_data), could use mmi_frm_set_xxx after this function.
 *  Note: We don't send any EVT_ID (ex. EVT_ID_GROUP_INACTIVE) to the new 
 *  inserted node. The inserted node state will be MMI_SCENARIO_STATE_INACTIVE.
 * PARAMETERS
 *  parent_id :         [IN] The parent group ID will have the new group.
 *                           If the parm is GRP_ID_ROOT, it means the
 *                           new group(group_id) is the stand-alone scenario.
 *  base_id :           [IN] The base node(group or screen) ID.
 *  new_node_info :     [IN] The new node information.
 *  flag :              [IN] Decide where the new node will be inserted.
 *                           MMI_FRM_NODE_AFTER_FLAG: inserted new node after base_id
 *                           MMI_FRM_NODE_BEFORE_FLAG: inserted new node before base_id
 *                           MMI_FRM_NODE_AT_BEGINNING_FLAG: inserted new node at the beginning(bottom node) of group_id
 *                           MMI_FRM_NODE_AT_LATEST_FLAG: inserted new node at the latest(top node) of group_id
 * RETURN VALUES
 *  MMI_RET_OK: inserte the new node successfully.
 *  MMI_RET_FALSE: insert the new node fail.
 *****************************************************************************/
mmi_ret mmi_frm_group_insert (MMI_ID parent_id, MMI_ID base_id, mmi_frm_node_struct *new_node_info, mmi_scenario_node_flag flag);


/*****************************************************************************
 * FUNCTION
 *  mmi_frm_group_replace
 * DESCRIPTION
 *  This function is used for replace the group in scenario tree. 
 *  When the applications call this function, they need to define what MMI_ID
 *  wants to be replaced. The new group id created by mmi_frm_group_create()
 *  is assigned in mmi_frm_node_struct. If the applications want to assign 
 *  more information (ex. user_data), could use mmi_frm_set_xxx after this function.
 *  Note: We don't send any EVT_ID (ex. EVT_ID_GROUP_DELETE_REQ or 
 *  EVT_ID_GROUP_DEINIT) to the replace-out node.
 * PARAMETERS
 *  parent_id :         [IN] The parent group ID will have the new group.
 *                           If the parm is GRP_ID_ROOT, it means the
 *                           new group(group_id) is the stand-alone scenario.
 *  out_id :            [IN] The node(group or screen) id wants to be replaced
 *  new_node_info :     [IN] The new node information.
 * RETURN VALUES
 *  MMI_RET_OK: replace the new node successfully.
 *  MMI_RET_FALSE: replace the new node fail.
 *****************************************************************************/
mmi_ret mmi_frm_group_replace (MMI_ID parent_id, MMI_ID out_id, mmi_frm_node_struct *new_node_info);


/*****************************************************************************
 * FUNCTION
 *  mmi_frm_group_close
 * DESCRIPTION
 *  This function is used for close the group in scenario tree. 
 *  If the closed group is the active group, framework will find out the
 *  previous node and execute the goback process 
 *  (EVT_ID_GROUP_GOBACK, EVT_ID_GROUP_INACTIVE, EVT_ID_GROUP_DEINIT).
 *  If the closed group isn't in the active group list, framework will delete
 *  the group in the scenario tree (EVT_ID_GROUP_DELETE_REQ, EVT_ID_GROUP_DEINIT).
 *  When the framework close the group, framework will emit mmi_scenario_evt_enum
 *  to the group proc.
 * PARAMETERS
 *  parent_id :         [IN] The group ID.
 * RETURN VALUES
 *  MMI_RET_OK: close the group successfully.
 *  others: the return value from the group proc that don't allow to close it.
 *****************************************************************************/
mmi_ret mmi_frm_group_close (MMI_ID group_id);

