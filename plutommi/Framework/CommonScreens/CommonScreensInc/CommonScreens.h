/*****************************************************************************
 * FUNCTION
 *  mmi_display_popup
 * DESCRIPTION
 *  1. Displays the common pop-up screen used across all applications.
 *  2. This pop-up screen will not be added into history.
 *  3. It will GoBackHistory when user press any key or timer expired.
 *  4. Get image, tone and duration based on event_id from table in custom_notify_event.c
 *  5. If the tone is over timer's duration, the popup duration time will the same as that of tone.
 * PARAMETERS
 *  string          : [IN](POPUP)    Char array of string
 *  event_id        : [IN](N/A)		   Display popup event type
 * RETURNS
 *  void
 *****************************************************************************/
    extern void mmi_display_popup(UI_string_type string, mmi_event_notify_enum event_id);
    
/*****************************************************************************
 * FUNCTION
 *  mmi_display_popup_with_tone
 * DESCRIPTION
 *  1. Displays the pop-up screen used across all applications.
 *  2. This pop-up screen will not be added into history.
 *  3. It will GoBackHistory when user press any key or timer expired.
 *  4. Get image and duration based on event_id from table in custom_notify_event.c
 *  5. If the tone is over timer's duration, the popup duration time will the same as that of tone.
 *  6. The tone use the input.
 * PARAMETERS
 *  string          : [IN](POPUP)    	Char array of string
 *  event_id        : [IN](N/A)    		Display popup type
 *  tone            : [IN](N/A)	    	Tone defined by app
 * RETURNS
 *  void
 *****************************************************************************/
    extern void mmi_display_popup_with_tone(UI_string_type string, mmi_event_notify_enum event_id, srv_prof_tone_enum tone);

/*****************************************************************************
 * FUNCTION
 *  mmi_display_popup_with_flag
 * DESCRIPTION
 *  1. Displays the pop-up screen used across all applications.
 *  2. This pop-up screen will not be added into history.
 *  3. It will GoBackHistory when user press any key or timer expired.
 *  4. Get image, tone and duration based on event_id from table in custom_notify_event.c
 *  5. If the tone is over timer's duration, the popup duration time will the same as that of tone.
 *  6. Set popup behaviour by flag. It can be MMI_POPUP_DISABLE_SLIDING_EFFECT ,MMI_POPUP_DISABLE_SHOW_ICON. 
 * PARAMETERS
 *  string          : [IN](POPUP)    Char array of string
 *  event_id        : [IN](N/A)		   Display popup type
 *  flag            : [IN](N/A)    	 Popup behaviour flag
 * RETURNS
 *  void
 *****************************************************************************/
    extern void mmi_display_popup_with_flag(UI_string_type string, mmi_event_notify_enum event_id, U32 flag);

/*****************************************************************************
 * FUNCTION
 *  mmi_display_popupcallback
 * DESCRIPTION
 * 1. This pop-up screen will not be added into history.
 * 2. This pop-up will not GoBackHistory, and applications have to handle the next screen by callback function f.
 * 3. It will call callback f when timer expired or pressing any key or pen down.
 * 4. If there any incoming event that need show new screen, we will not call f.
 * 5. Get image, tone and duration based on event_id from table in custom_notify_event.c
 * PARAMETERS
 *  string              : [IN](POPUP)   Char array of string
 *  event_id            : [IN](N/A)    	Display popup type
 *  f                   : [IN](N/A)    	Callback function pointer
 * RETURNS
 *  void
 *****************************************************************************/
    extern void mmi_display_popupcallback(UI_string_type string, mmi_event_notify_enum event_id, FuncPtr f);

/*****************************************************************************
 * FUNCTION
 *  mmi_display_popupcallback_ext
 * DESCRIPTION
 * 1. This pop-up screen will not be added into history.
 * 2. This pop-up will not GoBackHistory, and applications have to handle the next screen by callback function f.
 * 3. It will call callback f only when timer expired or pressing any key or pen down.
 * 4. If there any new incoming event that need show new screen, we will not call f.
 * 5. interr_callback function will be called only when condition 4.
 * 6. It's a strict rule that interr_callback can't handle screen ;interr_callback  only handle low_cost event for App.
 * PARAMETERS
 *  string                  : [IN](POPUP)    	Char array of string
 *  mmi_event_notify_enum   : [IN](N/A)    		Popup event type
 *  f                       : [IN](N/A)    		Callback function pointer when timer expired
 *  interr_callback         : [IN](N/A)    		Callback function pointer when exit the popup screen only by interrupt.
 * RETURNS
 *  void
 *****************************************************************************/
    extern void mmi_display_popupcallback_ext(UI_string_type string, mmi_event_notify_enum event_id, FuncPtr f, FuncPtr interr_callback);

