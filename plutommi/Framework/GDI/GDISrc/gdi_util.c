/*****************************************************************************
 * FUNCTION
 *  gdi_fast_semi_crc
 * DESCRIPTION
 *  
 * PARAMETERS
 *  src         [?]         
 *  size        [IN]        
 * RETURNS
 *  
 *****************************************************************************/
U32 gdi_fast_semi_crc(U8 *src, U32 size)
{
    /*----------------------------------------------------------------*/
    /* Local Variables                                                */
    /*----------------------------------------------------------------*/
    register U32 crc, temp, i;

    /*----------------------------------------------------------------*/
    /* Code Body                                                      */
    /*----------------------------------------------------------------*/
    crc = 0;
    for (i = 0; i < size; i++)
    {
        if (crc >> 15)
        {
            temp = 1;
        }
        else
        {
            temp = 0;
        }
        crc = (((crc << 1) & 0x0ffff) + temp) ^ (U32) * src;
        src++;
    }
    return crc;
}

